# Manipulate strings

first_name = 'Nazar'
last_name = 'Klypych'

# almost standard print function
print('Hello,', first_name, last_name + '!')

# concatenation
print('Hello,' + ' ' + first_name + ' ' + last_name + '!')

# using .join() method
print(' '.join(('Hello,', first_name, last_name + '!')))
